    <p><span class="header">Gennady Verkhivker</span>
     <br>
     <img src="../images/verkhivker.png" width="49" height="72" alt=""/><br>
     Dr. Verkhivker earned his PhD in theoretical physical chemistry from the Institute of Physical Chemistry, Russian National Academy of Sciences in 1986. He received his postdoctoral training in computational biophysics and theoretical chemistry in the Institute of Biophysics, Russian National Academy of Sciences (1986-1989) in at the University of Illinois at Chicago (1990-1992). Upon starting his industrial career at Agouron Pharmaceuticals in 1993, Dr. Verkhivker established a distinguished professional record in pharmaceutical industry. He played a leading role in establishing computer- aided drug design technology and building the largest computational group in pharmaceutical industry. In 1993-2006, Dr. Verkhivker has held various research and management positions of increasing responsibility at Agouron Pharmaceuticals, Warner- Lambert, Pfizer Global Research and Development, La Jolla Laboratories. He has provided scientific leadership to multidisciplinary research teams at Warner-Lambert Inc., and Pfizer Global Research and Development, La Jolla Laboratories in the development of computer-based drug discovery technologies and design of anti-cancer therapies.  In 2006-2010, Dr. Verkhivker served as Professor of Bioinformatics at the School of Pharmacy, The University of Kansas. He joined faculty at Chapman University in 2011. Since 2002, he has been Adjunct Professor of Pharmacology at the Department of Pharmacology, University of California, and San Diego.   </p>
