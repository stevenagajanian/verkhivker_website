<p>
  Nathaniel Bischoff is a current senior Biological Sciences major at Chapman University, and will be in the Masters of Computational Sciences program full time this coming fall. 
  He will look to be focusing in Bioinformatics and Computational Biology for his masters.
  In his four years at Chapman, he has gained a background in biology and related sciences (chemistry, biochemistry, and physics) while also taking courses in both computer science and mathematics. 
  Outside of his academics, Nathaniel has been very involved at Chapman University. 
  He has worked for the Office of Admission since January 2014, where he has given tours, worked as an Admissions Fellow, and served as an Office Assistant. 
  Since the beginning of sophomore year, Nathaniel has worked for the Office of Housing and Residence Life as RHA President, an RA, and now a Lead Resident Advisor. 
  He has been a part of Delta Tau Delta, the national fraternity, since spring of his freshman year. 
  Nathaniel has completed two internships serving over 300 hours at Hoag Hospital in Newport Beach as a Critical Care Extender, and over 150 hours at CHOC Children’s in Orange as a MI3 Intern. 
  At CHOC, Nathaniel presented at Grand Rounds continuing education on new ways to  <a href="http://tinyurl.com/jqwyfrt"> administer vaccines </a>. 
  In January 2016, Nathaniel presented <a href="http://tinyurl.com/j3os3mz"> abstract for real time prediction analysis </a> as it relates to congenital heart disease at <a href = "https://peds2040.org"> PEDS2040</a>. 
  Nathaniel hopes to one day use his focus in bioinformatics to help with patient care, improving diagnosis and predicting disease before it even starts.
