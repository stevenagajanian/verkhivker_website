   <img src="../images/gabyStetz.jpg" alt="" width="400" height="349" class="floatLeft" style="padding-bottom: 7em; padding-top: 0em;"/>Gabrielle Stetz is currently a senior at Chapman University pursuing a degrees in Biochemistry and Molecular Biology as well as Music. She is involved in many activities on campus, from the Chapman University Honors Program, Tri Beta Biological Honors Society, to the Mortar Board Honors Society and Kappa Alpha Theta, an international fraternity for women. Gabrielle was honored to receive both a Presidential Scholarship as well as a Conservatory of Music Talent Award to be able to attend Chapman University. 
   <p>Gabrielle has future goals of pursuing a career in medicine and will be applying to medical schools in the spring. Her work with the Verkhivker Research Group has led her to complete the Chapman Summer Undergraduate Research Fellowship this past year and spend ten weeks working in-depth on various projects. Her experiences in undergraduate research have been an important part of her development as a scientist and a cornerstone of her university education. Outside of academia, Gabrielle also works as a Research and Innovation Contractor for Medtronic, Inc. where she is able to use her research abilities in an industry setting. </p>
   <p>Outside of her passion for the sciences, Gabrielle is a conservatory-level violinist currently serving as the Co-Principal 2nd Violinist of the Chapman Orchestra. She enjoys playing in multiple ensembles throughout the year - quartets, orchestra, and opera among others - and always hopes to keep music as a part of her life. Her goal is to one day be playing violin for a doctor's orchestra. </p>
