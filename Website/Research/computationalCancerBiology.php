   <p class="header">Computational Cancer Biology</p>
   <p class="text_box"><span class="sub_ctrXS"><br>
     Computational Cancer Biology</span><br>
     The challenge of understanding biological systems at the molecular and systems level as well as the integration of   <img src="../images/compCancerBioBanner.jpg" alt="" width="260" height="206" class="floatRight"/>computational   and experimental approaches for bridging basic and clinical cancer research is what motivates our vibrant  research group. Our  scientific interests and research efforts are in the areas of Computational  Cancer Biology and Pharmacogenetics, Computational Genomics and Pharmacology, Translational Bioinformatics and Computational Medicine   with the focus  on the development and  integration of computational and experimental  approaches for (a) system-based analysis of evolutionary, genetic, molecular and clinical signatures associated with human disease; (b) modeling of complex phenotypes  and prediction of cancer biomarkers;  (c)  design and discovery of targeted and personalized cancer therapeutics  and development of expert systems for personalized medicine;  (d) integration of  computational biology and translational  informatics with   chemical biology and  chemical genomics in translational cancer research; (e) enabling   information-driven biomedical  research  on the “bench to bedside” path.</p>
   <p class="text_box"><span class="sub_ctrXS">Main scientific themes of the research program:</span>   </p>
   <div class="text_box">
     <h3>Computational Cancer Biology and Pharmacogenetics<br>
     </h3>
    <br>
     <ul><li>Integrative analysis of genetic  and molecular signatures of human disease at sequence, structure, functional and clinical levels for understanding the molecular basis of  cancer and developing new tools for translational research.</li>
     <li>Computational chemical genomics and  pharmacogenetics : development  computational approaches and tools  for the identification, prediction and functional analysis of cancer variants  to enable design of personalized cancer medicine targeting  specific genomic profiles.</li>
    <li>Pathway-based  and network-based approaches for analysis of human disease to identify functionally related gene modules targeted by somatic mutations in cancer.</li></ul></div><br>
    <div class="text_box">
     <h3>Translational Bioinformatics and Computational Medicine<br>
     </h3>
    <br>
     <ul><li>Translational bioinformatics approaches in the genome-wide functional analysis of cancer variants and prediction of cancer biomarkers.</li>
     <li>Computational genomics, proteomics and systems biology approaches for molecular profiling and drug discovery of protein kinases and molecular chaperone inhibitors.</li>
     <li>Targeted polypharmacology of signal transduction networks and pathway-targeted discovery of anti-cancer therapeutics.</li>
     <li>Integration of  computational biology and translational  informatics within the discovery of personalized anti-cancer cancer agents targeting specific genomic profiles</li>
    <li>Development of knowledge-based personalized medicine decision systems for clinical and translational research.</li></ul></div>
