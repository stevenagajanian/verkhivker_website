   <p> </p>
   <p class="sub_ctrXS">Computational Studies of the Hsp90 Chaperone and Cancer Research</p>
   <p class="text_box">Our Research is focused on the development and integration of computational and experimental approaches with the goal to advance understanding of the molecular chaperone mechanisms at atomic resolution and facilitate discovery of novel anti-cancer therapies.</p>
   <p> </p>
   <p class="sub_ctrXS">Our Projects are :</p>
   <p class="text_box">Development of  novel computationalapproaches for molecular modeling of Hsp90dynamics, ligand-based allosteric modulation and inter-domain communication.<br>
     Integration of  computational and experimental approaches into a system biology platform for structure-based characterization of Hsp90 binding mechanisms.<br>
     Computer-based ligand screening, design and biological validation of novel Hsp90 inhibitors<br>
     <br>
     These approaches allowed  to  develop and validate  mathematical and  structural models of Hsp90 allosteric regulation  and signal communication pathwayS. Client proteins of Hsp90 include protein kinases, transcription factors, and other proteins that serve as nodal points in integrating cellular responses to multiple signals. Deregulation of pathways involving these proteins is commonly associated with cancer pathologies. By disabling multiple signaling circuitries, Hsp90 inhibition provides a novel and powerful therapeutic strategy in cancer research, selective for specific cancer mechanisms, yet broadly applicable to disparate tumors with different genetic signatures. Hsp90 Inhibition can suppresses signaling of kinase cancer  mutant clients and overcomes drug resistance.  Computational profiling and  systems-based approaches are integrated with experimental strategies  into a systematic platform for  selective targeting of  Hsp90-kinase protein networks. The developed allosteric Hsp90 modulators  can function as specific and personalized therapeutics  for inhibiting protein kinase clients and cancer mutants. We have established a close partnership and are involved in collaborative efforts with a number of prominent Hsp90 research groups including Dr. Matts (Oklahoma State University), Dr. Neckers (National Cancer Institute), Dr. Altieri (University of Massachusetts Medical School), Dr. Agard (UCSF), Dr. Pearl (The Institute of Cancer Research, UK) and others. The expertise   of our research team   and cross-disciplinary collaborations with the Hsp90 experts in molecular and cell biology, biochemistry, NMR, and structural biology will help to develop and validate computational approaches, refine and enhance experimental tools to advance our understanding of the molecular chaperone mechanisms.The insights about  molecular mechanisms and function of molecular chaperone are employed  in chemical genomics-based  profiling, design and biological validation of novel therapeutics of signal transduction networks.</p>
