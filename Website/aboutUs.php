   <p><span class="header">About Us</span><br>
     Computational Biology and Translational Bioinformatics in Cancer Research: From Understanding Genetic and Molecular Signatures of Human Disease to Personalized Medicine</p>
   <p class="text_box">The challenge of understanding biological systems at the molecular and systems level as well as the integration of   computational   and experimental approaches for bridging basic and clinical cancer research is what motivates our vibrant  research group. Our  scientific interests and research efforts are in the areasof Computational  Cancer Biology and Pharmacogenetics, Computational Genomics and Pharmacology, Translational Bioinformatics and Computational Medicine   with the focus  on the development and  integration of computational and experimental  approaches for (a) system-based analysis of evolutionary, genetic, molecular and clinical signatures associated with human disease; (b) modeling of complex phenotypes  and prediction of cancer biomarkers;  (c)  design and discovery of targeted and personalized cancer therapeutics  and development of expert systems for personalized medicine;  (d) integration of  computational biology and translational  informaticswith   chemical biology and  chemical genomics in translational cancer research; (e) enabling   information-driven biomedical  research  on the “bench to bedside” path.<br>
   </p>
   <p class="sub_ctr">Main scientific themes of the research program:</p>
   <p class="sub_ctrXS"><a href="Research/computationalCancerBiology.php">Computational Cancer Biology and Pharmacogenetics</a></p>
   <p class="text_box">·         Integrative analysis of genetic  and molecular signatures of human diseaseat sequence, structure, functional and clinical levels for understanding the molecular basis of  cancer and developing new tools for translational research.</p>
   <p class="text_box">·         Computational chemical genomics and  pharmacogenetics : development  computational approaches and tools  for the identification, prediction and functional analysis of cancer variants  to enable design of personalized cancer medicine targeting  specific genomic profiles.</p>
   <p class="text_box">·         Pathway-based  and network-based approaches for analysis of human diseaseto identify functionally related gene modules targeted by somatic mutations in cancer.</p>
   <p class="sub_ctrXS"><a href="Research/translationalBioinformatics.php">Translational Bioinformatics and Computational Medicine</a></p>
   <p class="text_box">·         Translational bioinformatics approachesin the genome-wide functionalanalysis of cancer variants and prediction of cancer biomarkers.</p>
   <p class="text_box">·         Computational genomics, proteomics and systems biology approachesfor molecular profiling and drug discovery of protein kinases and molecular chaperone inhibitors.</p>
   <p class="text_box">·         Targeted polypharmacology of signal transduction networksand pathway-targeted discovery of anti-cancer therapeutics.</p>
   <p class="text_box">·         Integration of  computational biology and translational  informaticswith   chemical biology and  chemical genomics in the discovery of personalized anti-cancer cancer agents targeting specific genomic profiles</p>
   <p class="text_box">·         Development of knowledge-based personalized medicine decision systemsfor clinical and translational research.</p>
